l = [1, 2, 3]
print(f"l: {l}")

l_times_5 = l * 5
print(f"l * 5: {l_times_5}")

abcd_times_5 = 5 * 'abcd'
print(f"5 * 'abcd': {abcd_times_5}")

my_list = [[]] * 3
print(f"[[]] * 3: {my_list}")

others = [4, 5]
print(f"others: {others}")

l_plus_others = l + others
print(f"l + others: {l_plus_others}")
